const orderedKnownPhrases = Object.entries({ ...termMap, ...footballNameMap }).sort(([left], [right]) => right.length - left.length);

export function hasLatinText(value: unknown): boolean {
  return /[A-Za-zÀ-ž]/u.test(String(value ?? ""));
}

export function toChineseDisplay(value: unknown, fallback = "待补中文名"): string {
  if (value === null || value === undefined) return "";
  let result = String(value);

  for (const [source, target] of orderedKnownPhrases) {
    result = replacePhrase(result, source, target);
    const stripped = stripDiacritics(source);
    if (stripped !== source) {
      result = replacePhrase(result, stripped, target);
    }
  }

  if (!hasLatinText(result)) return result;
  if (/^[A-Za-zÀ-ž0-9'’.\-\s]+$/u.test(result.trim())) return fallback;

  return result
    .replace(/[A-Za-zÀ-ž][A-Za-zÀ-ž0-9'’._/-]*/gu, "未接入中文名")
    .replace(/(未接入中文名[\s,，、:：/\\-]*){2,}/g, "未接入中文名")
    .replace(/\s+/g, " ")
    .trim();
}

function replacePhrase(value: string, source: string, target: string): string {
  const escaped = source.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return value.replace(new RegExp(`(^|[^A-Za-zÀ-ž])(${escaped})(?=$|[^A-Za-zÀ-ž])`, "giu"), `$1${target}`);
}

function stripDiacritics(value: string): string {
  return value.normalize("NFD").replace(/\p{Diacritic}/gu, "");
}
