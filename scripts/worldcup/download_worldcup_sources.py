from __future__ import annotations

import argparse
import hashlib
import json
import time
from dataclasses import dataclass
from pathlib import Path
from urllib.request import Request, urlopen


ROOT = Path(__file__).resolve().parents[2]
RAW_ROOT = ROOT / "data" / "worldcup" / "raw"
MANIFEST_PATH = RAW_ROOT / "source_manifest.json"


@dataclass(frozen=True)
class SourceFile:
    provider: str
    file_name: str
    url: str

    @property
    def output_path(self) -> Path:
        return RAW_ROOT / self.provider / self.file_name


SOURCES = [
    SourceFile(
        "fjelstul",
        "matches.csv",
        "https://raw.githubusercontent.com/jfjelstul/worldcup/master/data-csv/matches.csv",
    ),
    SourceFile(
        "fjelstul",
        "goals.csv",
        "https://raw.githubusercontent.com/jfjelstul/worldcup/master/data-csv/goals.csv",
    ),
    SourceFile(
        "fjelstul",
        "tournament_stages.csv",
        "https://raw.githubusercontent.com/jfjelstul/worldcup/master/data-csv/tournament_stages.csv",
    ),
    SourceFile(
        "fjelstul",
        "host_countries.csv",
        "https://raw.githubusercontent.com/jfjelstul/worldcup/master/data-csv/host_countries.csv",
    ),
    SourceFile(
        "martj42",
        "results.csv",
        "https://raw.githubusercontent.com/martj42/international_results/master/results.csv",
    ),
    SourceFile(
        "martj42",
        "shootouts.csv",
        "https://raw.githubusercontent.com/martj42/international_results/master/shootouts.csv",
    ),
]


def download_source(source: SourceFile, force: bool = False, timeout: int = 45) -> dict[str, object]:
    output_path = source.output_path
    output_path.parent.mkdir(parents=True, exist_ok=True)
    if output_path.exists() and not force:
        data = output_path.read_bytes()
        status = "kept_existing"
    else:
        request = Request(source.url, headers={"User-Agent": "football-prediction-platform-etl"})
        with urlopen(request, timeout=timeout) as response:
            data = response.read()
        if not data:
            raise RuntimeError(f"Downloaded empty file from {source.url}")
        output_path.write_bytes(data)
        status = "downloaded"

    return {
        "provider": source.provider,
        "file": source.file_name,
        "path": str(output_path.relative_to(ROOT)).replace("\\", "/"),
        "url": source.url,
        "status": status,
        "bytes": len(data),
        "sha256": hashlib.sha256(data).hexdigest(),
    }


def write_manifest(records: list[dict[str, object]]) -> None:
    MANIFEST_PATH.parent.mkdir(parents=True, exist_ok=True)
    payload = {
        "generated_at_unix": int(time.time()),
        "source_policy": "Public source files only. No fabricated matches, scores, events, or lineups are generated by this downloader.",
        "records": records,
    }
    MANIFEST_PATH.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Download verified public World Cup enhancer source CSV files")
    parser.add_argument("--force", action="store_true", help="Overwrite existing source files")
    parser.add_argument("--timeout", type=int, default=45)
    args = parser.parse_args()

    records = [download_source(source, force=args.force, timeout=args.timeout) for source in SOURCES]
    write_manifest(records)
    for record in records:
        print(f"{record['status']}: {record['path']} ({record['bytes']} bytes, sha256={str(record['sha256'])[:12]})")
    print(f"Wrote source manifest: {MANIFEST_PATH.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
